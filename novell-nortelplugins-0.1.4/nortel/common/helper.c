#include <stdio.h>
#include <string.h>
#include <inttypes.h>
#include <sys/socket.h>
#include <netdb.h>
#include <netinet/in.h>
#include <arpa/inet.h>
#include <unistd.h>
#include <pwd.h>

#include "helper.h"

char *getUserHome(void)
{
	struct passwd *pw;
	if (!(pw = getpwuid(getuid())))
		return NULL;
	else
		return pw->pw_dir;
}
